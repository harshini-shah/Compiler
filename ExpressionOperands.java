
public class ExpressionOperands {
	private Result op1;
	private Result op2;
	
	public ExpressionOperands(Result op1, Result op2){
		this.op1 = op1;
		this.op2 = op2;
	}
	
	public Result getOp1() {
		return op1;
	}
	public void setOp1(Result op1) {
		this.op1 = op1;
	}
	public Result getOp2() {
		return op2;
	}
	public void setOp2(Result op2) {
		this.op2 = op2;
	}
	
	
}
